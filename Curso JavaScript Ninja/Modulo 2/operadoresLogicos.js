//&& (and)
var x = 3 
var y = 7

console.log(x === 3)
console.log(y === 7)
console.log(y === 7 && x == 3) 
console.log(y === 2 && x == 3) // ele junta os dois valores pra dar a resposta -> por isso da false mesmo x sendo 3

//  || (or)

console.log(x == 3 || y == 3) // um ou outro valor vai ser true 

// ! (not)
console.log( x != 3)
console.log( x !== 3)