console.log("Desafio Operacional: Calcular a média de 3 notas e verificar se o aluno foi aprovado ou reprovado.");

let nota1 = 4;
let nota2 = 8;
let nota3 = 6;

let media = (nota1 + nota2 + nota3) / 3;

if (media >= 7) {
    console.log("Aluno aprovado com média: " + media);
} else {
    console.log("Aluno reprovado com média: " + media);
}
